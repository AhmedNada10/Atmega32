#ifndef CLCD_CONFIG_H_
#define CLCD_CONFIG_H_

/********************************************************************************************************************************/

/********************************************************* MACROS section *******************************************************/
#define CLCD_MODE       CLCD_8BIT_MODE                  // options ->> 1: CLCD_4BIT_MODE   , 2:CLCD_8BIT_MODE
#define CLCD_CTR_PORT    PORTC                          // options --> (PORTA , PORTB , PORTC , PORTD)
#define CLCD_DAT_PORT    PORTD                          // options --> (PORTA , PORTB , PORTC , PORTD)
#define RS_PIN            PIN2                          // options --> (PIN0 , PIN1 , PIN2 ,.....PIN7)
#define RW_PIN            PIN1                          // options --> (PIN0 , PIN1 , PIN2 ,.....PIN7)
#define E_PIN             PIN0                          // options --> (PIN0 , PIN1 , PIN2 ,.....PIN7)

/*******************************************************************************************************************************/
#if CLCD_MODE == CLCD_4BIT_MODE
    #define FUNCTION_SET  0xC0
    #define DISPLAY_ON_OFF 0xC0
    #define DISPLAY_CLR  0x10
    #define ENTRY_MODE 0x70
#elif CLCD_MODE == CLCD_8BIT_MODE
    #define FUNCTION_SET  0x38
    #define DISPLAY_ON_OFF 0x0C
    #define DISPLAY_CLR  0x01
    #define ENTRY_MODE 0x06
#endif



#endif /* CLCD_CONFIG_H_ */





